/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class BSTIterator {

    List<Integer> list;
    int pointer;

    public BSTIterator(TreeNode root) {
        this.list = new ArrayList<>();
        dfs(root, list);
        this.pointer = 0;
    }

    public void dfs(TreeNode root, List<Integer> list) {
        if (root == null) return;
        dfs(root.left, list);
        list.add(root.val);
        dfs(root.right, list);
    }
    
    public int next() {
        int ret = list.get(pointer); 
        pointer++;
        return ret;
    }
    
    public boolean hasNext() {
        return pointer <= list.size() - 1;
    }
}

